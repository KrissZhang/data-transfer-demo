package com.self.datatransferclient.constant;

/**
 * 日期格式
 */
public final class DateFormat {

    /**
     * 精确到三位毫秒
     */
    public static final String FORMAT_YYYYMMDD_HHMMSS_SSS = "yyyyMMddHHmmssSSS";

    /**
     * 精确到天(中横线分割)
     */
    public static final String FORMAT_YYYY_MM_DD = "yyyy-MM-dd";

    /**
     * 精确到天
     */
    public static final String FORMAT_YYYYMMDD = "yyyyMMdd";

    /**
     * 精确到三位毫秒(中横线分割日期)
     */
    public static final String FORMAT_YYYY_MM_DD_HHMMSSSSS = "yyyy-MM-dd HH:mm:ss.SSS";

    /**
     * 精确到秒
     */
    public static final String FORMAT_YYYY_MM_DD_HHMMSS = "yyyy-MM-dd HH:mm:ss";

    /**
     * 精确到秒(T分割)
     */
    public static final String FORMAT_YYYY_MM_DD_T_HHMMSS = "yyyy-MM-dd'T'HH:mm:ss";

    /**
     * 精确到天(斜杠分割)
     */
    public static final String FORMAT_YYYY_MM_DD_SLASH = "yyyy/MM/dd";

    /**
     * 东八区
     */
    public static final String FORMAT_GMT_8 = "GMT+8";

}
